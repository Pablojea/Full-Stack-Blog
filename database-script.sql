CREATE TABLE IF NOT EXISTS comments
(
	cid INT PRIMARY KEY NOT NULL AUTO_INCREMENT,
    pid INT,
    uid INT,
    body VARCHAR(1000),
    FOREIGN KEY (pid) REFERENCES posts(pid),
    FOREIGN KEY (uid) REFERENCES users(uid)
);

