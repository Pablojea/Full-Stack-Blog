CREATE TABLE IF NOT EXISTS posts
(
	pid INT PRIMARY KEY NOT NULL AUTO_INCREMENT,
    title VARCHAR(100),
    brief VARCHAR(300),
    body VARCHAR(1000),
    image VARCHAR(1000),
    uid INT,
    FOREIGN KEY (uid) REFERENCES users(uid)
);

